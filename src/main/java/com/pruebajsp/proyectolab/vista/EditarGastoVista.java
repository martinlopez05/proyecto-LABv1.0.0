    /**
     * Método que se ejecuta cuando se hace clic en el botón "Modificar". Valida
     * los campos de entrada y actualiza el gasto en la base de datos.
     *
     * @param evt Evento de acción generado por el clic en el botón.
     */
    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        Gasto gasto = control.traerGasto(this.idGasto);
        String nombre = txtNombre.getText().trim();
        Date fecha = dateFecha.getDate();
        String pagoTxt = txtPago.getText().trim();
        String debeTxt = txtDebe.getText().trim();

        if (nombre.isEmpty() || fecha == null
                || debeTxt.isEmpty() || pagoTxt.isEmpty()) {

            VistaUtils.mostrarMensaje("Hay campos sin completar", "error", "Error al guardar");
            return;
        }

        try {

            Double pago = Double.parseDouble(pagoTxt);
            Double debe = Double.parseDouble(debeTxt);
            control.editGasto(gasto, nombre, debe, pago, fecha);
            VistaUtils.mostrarMensaje("Gasto editado correctamente", "info", "Edición exitoso");
            this.dispose();

        } catch (NumberFormatException e) {
            VistaUtils.mostrarMensaje("Ingrese valores numéricos válidos en los campos Debe y Pago", "error", "Error en los datos");
        } catch (Exception ex) {
            Logger.getLogger(EditarGastoVista.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnModificarActionPerformed

