    /**
     * Método que carga los datos del gasto en los campos de la interfaz
     * gráfica. Obtiene los datos del gasto a través de la controladora y los
     * muestra en los campos correspondientes.
     */
    public void cargarDatosGasto() {
        Gasto gasto = control.traerGasto(idGasto);
        txtNombre.setText(gasto.getNombreProveedor());
        txtDebe.setText(String.valueOf(gasto.getDebe()));
        txtPago.setText(String.valueOf(gasto.getPago()));
        dateFecha.setDate(gasto.getFecha());
    }


