    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        Odontologo odontologo = control.traerOdontologo(this.idOdontologo);
        String nombre = txtNombre.getText().trim();
        String direccionTxt = txtDireccion.getText().trim();

        if (nombre.isEmpty() || direccionTxt.isEmpty()) {
            VistaUtils.mostrarMensaje("Hay campos sin completar", "error", "Error al guardar");
            return;
        }
        try {
            control.editOdontologo(odontologo, nombre, direccionTxt);
            VistaUtils.mostrarMensaje("Odontologo editado correctamente", "info", "Edici√≥n exitoso");
            this.dispose();
        } catch (Exception ex) {
            Logger.getLogger(EditarOdontologoVista.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnModificarActionPerformed

