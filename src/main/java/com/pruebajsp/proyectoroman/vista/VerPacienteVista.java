    /**
     * Método que se ejecuta cuando se hace clic en el botón "Crear Paciente".
     * Abre la vista para crear un nuevo paciente asociado al odontólogo
     * seleccionado.
     *
     * @param evt Evento de acción generado por el clic en el botón.
     */
