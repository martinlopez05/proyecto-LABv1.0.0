    /**
     * Método que carga los datos de los odontólogos en la tabla. Obtiene los
     * datos de la controladora y los muestra en la tabla.
     */
    public void cargarTablaOdontologo() {
        tablaOdontologos.setRowHeight(30);
        DefaultTableModel modelotabla = new DefaultTableModel() {

            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        List<Odontologo> odontologos = control.traerListaOdontologos();
        System.out.println("Pacientes encontrados: " + odontologos.size());

        String titulo[] = {"id", "Nombre", "Dirección"};
        modelotabla.setColumnIdentifiers(titulo);

        for (Odontologo odontologo : odontologos) {
            Object objeto[] = {odontologo.getIdOdontologo(), odontologo.getNombre(), odontologo.getDireccion()};
            modelotabla.addRow(objeto);
        }

        tablaOdontologos.setModel(modelotabla);
        // Suponiendo que "tablaPacientes" es tu JTable
        TableColumnModel columnModel = tablaOdontologos.getColumnModel();

        // Ajustamos el ancho de cada columna
        /*tablaOdontologos.getColumnModel().getColumn(0).setMinWidth(0);
        tablaOdontologos.getColumnModel().getColumn(0).setMaxWidth(0);*/
        tablaOdontologos.getColumnModel().getColumn(0).setWidth(5);   // ID (más pequeño)
        columnModel.getColumn(1).setPreferredWidth(150);
        columnModel.getColumn(2).setPreferredWidth(250);
        tablaOdontologos.setBackground(new java.awt.Color(255, 255, 255));
        tablaOdontologos.getTableHeader().setFont(new Font("Arial", Font.BOLD, 12));
        tablaOdontologos.setFont(new java.awt.Font("Arial", 0, 12));// Nombre (más grande)

    }


