    /**
     * Método que se ejecuta cuando se hace clic en el botón "Modificar". Valida
     * los campos de entrada y actualiza el paciente en la base de datos.
     *
     * @param evt Evento de acción generado por el clic en el botón.
     */
    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        Paciente paciente = control.traerPaciente(this.idPaciente);
        String nombre = txtNombre.getText().trim();
        Date fecha = dateFecha.getDate();
        String cantidadTxt = txtCantidad.getText().trim();
        String pagoTxt = txtPago.getText().trim();
        String debeTxt = txtDebe.getText().trim();
        String trabajo = txtTrabajo.getText();

        if (nombre.isEmpty() || trabajo.isEmpty() || fecha == null
                || cantidadTxt.isEmpty() || pagoTxt.isEmpty()) {

            VistaUtils.mostrarMensaje("Hay campos sin completar", "error", "Error al guardar");
            return;
        }

        try {
            int cantidad = Integer.parseInt(cantidadTxt);
            Double pago = Double.parseDouble(pagoTxt);
            Double debe = Double.parseDouble(debeTxt);

            control.editPaciente(paciente, nombre, trabajo, cantidad, debe, pago, fecha);
            VistaUtils.mostrarMensaje("Paciente editado correctamente", "info", "Edición exitoso");
            this.dispose();

        } catch (NumberFormatException e) {
            VistaUtils.mostrarMensaje("Ingrese valores numéricos válidos en los campos de cantidad, pago, saldo, debe y total trabajo", "error", "Error en los datos");
        } catch (Exception ex) {
            Logger.getLogger(EditarPacienteVista.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnModificarActionPerformed

