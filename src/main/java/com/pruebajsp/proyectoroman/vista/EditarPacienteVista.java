    /**
     * Carga los datos del paciente en los campos de la interfaz gráfica.
     * Obtiene los datos del paciente a través de la controladora utilizando el
     * identificador del paciente y los muestra en los campos correspondientes
     * de la interfaz.
     *
     * Los campos que se actualizan incluyen: - Nombre del paciente. - Trabajo
     * del paciente. - Monto adeudado. - Monto pagado. - Cantidad. - Fecha.
     *
     * Este método se utiliza para prellenar los campos de edición con la
     * información actual del paciente.
     */
    public void cargarDatosPaciente() {
        Paciente paciente = control.traerPaciente(idPaciente);
        txtNombre.setText(paciente.getNombre());
        txtTrabajo.setText(paciente.getTrabajo());
        txtDebe.setText(String.valueOf(paciente.getDebe()));
        txtPago.setText(String.valueOf(paciente.getPago()));
        txtCantidad.setText(String.valueOf(paciente.getCantidad()));
        dateFecha.setDate(paciente.getFecha());
    }


